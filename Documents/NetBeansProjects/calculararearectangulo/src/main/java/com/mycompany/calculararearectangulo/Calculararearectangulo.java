package com.mycompany.calculararearectangulo;

import java.lang.System;

public class Calculararearectangulo {
    public static void main(String[] args) {
        double base = 12.5; // Base del rectángulo
        double altura = 6.3; // Altura del rectángulo
        double area; // Variable para almacenar el área del rectángulo
        
        area = base * altura; // Fórmula para calcular el área de un rectángulo: base * altura
        
        System.out.println("El área del rectángulo es: " + area);
    }
}
